using System;

namespace OpenBookPgh
{
	/// <summary>
	/// Class Object for search parameters used in search
	/// </summary>
	public class SearchParamsLobbyists
	{
		public SearchParamsLobbyists() { }

		public SearchParamsLobbyists(int lobbyistID, string lobbyistKeywords, string companyKeywords)
		{
			this.lobbyistID = lobbyistID;
			this.lobbyistKeywords = lobbyistKeywords;
			this.companyKeywords = companyKeywords;
		}
		private int _lobbyistID = 0;
		public int lobbyistID
		{
			get { return _lobbyistID; }
			set { _lobbyistID = value; }
		}
		private string _lobbyistKeywords;
		public string lobbyistKeywords
		{
			get { return _lobbyistKeywords; }
			set { _lobbyistKeywords = value; }
		}
		private string _companyKeywords;
		public string companyKeywords
		{
			get { return _companyKeywords; }
			set { _companyKeywords = value; }
		}

	}
}